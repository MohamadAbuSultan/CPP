

#include <iostream>
using namespace std;
int main()
{
	int A, B;
	cout << "Please Enter The First Number" << endl;
	cin >> A; 
	cout << "Please Enter The Second Number" << endl;
	cin >> B;
	// + - * / %
	cout << A << " + " << B << " = " << A + B << endl;	
	cout << A << " - " << B << " = " << A - B << endl;
	cout << A << " * " << B << " = " << A * B << endl;
	cout << A << " / " << B << " = " << A / B << endl;
	cout << A << " % " << B << " = " << A % B << endl;


}
